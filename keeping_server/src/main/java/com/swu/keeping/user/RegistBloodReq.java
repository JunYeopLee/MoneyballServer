package com.swu.keeping.user;

public class RegistBloodReq {
	String id;
	String humanName;
	String birthDate;
	String bloodDate;
	String bloodLoc;
	String userId;
	String senderId;
	String bloodAmount;
	String bloodType;
	String bloodSex;
	String nextBloodDate;
	
	
	public void setId(String id) {
		this.id = id;
	}
	public void setHumanName(String humanName) {
		this.humanName = humanName;
	}
	public void setBirthDate(String birthDate) {
		this.birthDate = birthDate;
	}
	public void setBloodDate(String bloodDate) {
		this.bloodDate = bloodDate;
	}
	public void setNextBloodDate(String nextBloodDate) {
		this.nextBloodDate = nextBloodDate;
	}
	public void setBloodLoc(String bloodLoc) {
		this.bloodLoc = bloodLoc;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public void setSenderId(String senderId) {
		this.senderId = senderId;
	}
	public void setBloodAmount(String bloodAmount) {
		this.bloodAmount = bloodAmount;
	}
	public void setBloodType(String bloodType) {
		this.bloodType = bloodType;
	}
	public void setBloodSex(String bloodSex) {
		this.bloodSex = bloodSex;
	}
	
	

}
